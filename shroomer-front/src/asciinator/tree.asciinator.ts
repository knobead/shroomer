  public prepareTemplate(size: number, id: number): string
  {
    if (0 == size) {
      return TreeAsciinator.template_0[id % TreeAsciinator.template_0.length]
    }

    if (2 >= size) {
      return TreeAsciinator.template_1_2[id % TreeAsciinator.template_1_2.length]
    }

    return TreeAsciinator.template_3_4[id % TreeAsciinator.template_3_4.length]
  }

  public prepareTrunk(template: string): string
  {
    return template.replaceAll('/', "<span class='text-amber-700'>/</span>")
      .replaceAll('|', "<span class='text-amber-800'>|</span>")
      .replaceAll('\\', "<span class='text-amber-900'>\\</span>")
      .replaceAll('_', "<span class='text-amber-800'>_</span>")
  }

  public prepareLeaf(template: string, letter: string): string
  {
    let searchOne = 'z'
    let searchTwo = 'Z'

    if (Math.random() >= 0.5) {
      searchOne = 'Z'
      searchTwo = 'z'
    }

    return template.replaceAll(
      searchOne,
      "<span class='text-green-800 font-bold'>" + letter.toLowerCase() + "</span>"
    ).replaceAll(
      searchTwo,
      "<span class='text-green-700 font-bold'>" + letter.toUpperCase() + "</span>"
    )
  }
